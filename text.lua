local function stepToNext()
	if #coords == 0 then return end
	if index > #coords then index = 1 end
	local c = coords[index]
	local char = player.Character or player.CharacterAdded:Wait()
	local hrp = char:FindFirstChild("HumanoidRootPart")
	if hrp and c then
		TweenService:Create(hrp, TweenInfo.new(speed, Enum.EasingStyle.Linear),
			{CFrame = CFrame.new(Vector3.new(c.x, c.y, c.z))}):Play()
	end
	index += 1
end

task.spawn(function()
	while task.wait() do
		if autowalk then stepToNext() end
	end
end)

